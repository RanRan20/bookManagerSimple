     private void loginActionPerformed(java.awt.event.ActionEvent evt) {                                      
        String username = jUserField.getText();  
        String password = new String(JpasswordField.getPassword()); 

        if (adminUser.validate(username, password)) {
        javax.swing.JOptionPane.showMessageDialog(this, "ha iniciado sesion correctamente");
        openLibraryUI();
    } 
    
    else if (user.validate(username, password)) {
        javax.swing.JOptionPane.showMessageDialog(this, "ha iniciado sesion correctamente");
        openLibraryUserUi();
    } 
   
    else {
        javax.swing.JOptionPane.showMessageDialog(this, "usuario o contrase√±a incorrecta, intente de nuevo");
    }
    }

   
    private void openLibraryUI() {
        UI libraryUI = new UI();
        libraryUI.setLocationRelativeTo(null); 
        libraryUI.setVisible(true);
        this.dispose();
    }
    
     private void openLibraryUserUi() {
        UIuser userUI = new UIuser();
        userUI.setLocationRelativeTo(null); 
        userUI.setVisible(true);
        this.dispose();
    }
      
